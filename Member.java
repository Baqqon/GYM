    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {                                     
        // TODO add your handling code here:
        try {
            mf = (DefaultTableModel)jTable1.getModel();
            int selected = jTable1.getSelectedRow();
            int id = Integer.parseInt(mf.getValueAt(selected, 0).toString());
            txtmember.setText(mf.getValueAt(selected, 1).toString());
            txtemail.setText(mf.getValueAt(selected, 2).toString());
            txtaddress.setText(mf.getValueAt(selected, 3).toString());
            txtmobile.setText(mf.getValueAt(selected, 4).toString());
            txtage.setValue(Integer.valueOf((String)mf.getValueAt(selected, 5)));
            String mgender;
            mgender = mf.getValueAt(selected, 6).toString();
            if(mgender == "Male"){
                gmale.setSelected(true);
                gfemale.setSelected(false);
            }else if(mgender == "Female"){
                gfemale.setSelected(true);
                gmale.setSelected(false);
            }else{
                gmale.setSelected(false);
                gfemale.setSelected(false);
            }
            txttrainer.setSelectedItem(mf.getValueAt(selected, 7).toString());
            txtmtype.setSelectedItem(mf.getValueAt(selected, 8).toString());
          
            java.util.Date date = new SimpleDateFormat("yyyy-MM-dd").parse((String)mf.getValueAt(selected, 9));
            txtdoj.setDate(date);
            
            jButton2.setEnabled(false);
            
        } catch (ParseException ex) {
            Logger.getLogger(Trainer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }                                    

