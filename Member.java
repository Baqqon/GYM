    public void load_trainer(){
        try {
            pst = con.prepareStatement("select Distinct name from Trainer");
            ResultSet rs;
            
            rs = pst.executeQuery();
            
            txttrainer.removeAllItems();
            
            while(rs.next()){
                txttrainer.addItem(rs.getString("name"));
            }
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void table_load()
    {
        try {
            pst = con.prepareStatement("select * from Member");
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData rd = rs.getMetaData();
            int c;
            c = rd.getColumnCount();
            mf = (DefaultTableModel)jTable1.getModel();
            mf.setRowCount(0);
            jTable1.getColumnModel().getColumn(0).setPreferredWidth(10);
            jTable1.getColumnModel().getColumn(5).setPreferredWidth(20);
            jTable1.getColumnModel().getColumn(6).setPreferredWidth(40);
            jTable1.getColumnModel().getColumn(7).setPreferredWidth(40);
            jTable1.getColumnModel().getColumn(8).setPreferredWidth(50);
            while(rs.next())
            {
                Vector v2 = new Vector();
                
                for(int i = 1; i<=c; i++)
                {
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("name"));
                    v2.add(rs.getString("email"));
                    v2.add(rs.getString("address"));
                    v2.add(rs.getString("mobile"));
                    v2.add(rs.getString("age"));
                    v2.add(rs.getString("mgender"));
                    v2.add(rs.getString("trainer"));
                    v2.add(rs.getString("type"));
                    v2.add(rs.getString("doj"));
                            
                }
                
                mf.addRow(v2);
              
            }
        } catch (SQLException ex) {
            Logger.getLogger(Trainer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    


